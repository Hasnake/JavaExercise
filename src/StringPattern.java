public class StringPattern {
    public static void main(String[] args) {

        System.out.println("   FF  FFF      RRRRRRRR        II       DD DD DD                           aa                YYY          YY     ");
        System.out.println("   FF           RR     RR       II       DD       DD                     aa    aa               YYY       YYY    ");
        System.out.println("   FF  FF       RR     RR       II       DD        DD                   aa       aa              YYY    YYYY     ");
        System.out.println("   FF           RRRRRR          II       DD          DD               aaaaaaaaaaaaaaa                YYY          ");
        System.out.println("   FF           RR     RR       II       DD          DD              aaaaaaaaaaaaaaaaaa              YYY          ");
        System.out.println("   FF           RR      RR      II       DD         DD             aa                  aa            YYY          ");
        System.out.println("   FF           RR        RR    II       DD DD DD DD              aa                    aa           YYY          ");
    }
}

